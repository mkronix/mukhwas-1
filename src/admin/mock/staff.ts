function buildPerms(
  roleId: string,
  modules: string[],
  actions: Record<string, boolean>,
  surface: "admin" | "pos" = "admin"
): RolePermission[] {
  return modules.map((mod) => ({
    id: `perm_${roleId}_${surface}_${mod}`,
    role_id: roleId,
    module: mod,
    surface,
    can_create: actions.create ?? false,
    can_read: actions.read ?? false,
    can_update: actions.update ?? false,
    can_delete: actions.delete ?? false,
    can_export: actions.export ?? false,
    can_view_reports: actions.reports ?? false,
    extended_actions: [],
  }));
}

